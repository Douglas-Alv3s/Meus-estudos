#11. Validar CPF
#● CPF tem 11 digitos

#● Validação acontece utilizando os 9 primeiros digitos

#– Resultado do cálculo tem que ser igual aos dois ultimos

#● Primeiro digito

#– Multiplica-se os primeiros 9 digitos pela sequência decrescente de 
#números de 10 a 2 e soma o resultado

#– Multiplica-se o resultado por 10 e divide por 11

#– O resto da divisão deve ser igual ao primeiro digito verificador

#– (se o resto for 10, considera-se o valor 0)
